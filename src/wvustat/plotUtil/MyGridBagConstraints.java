package wvustat.plotUtil;

import java.awt.GridBagConstraints;
import java.awt.Insets;
/**
 *
 * @author  hxue
 * @version 
 */
public class MyGridBagConstraints extends GridBagConstraints {

    /** Creates new MyGridBagConstraints */
    public MyGridBagConstraints(int gridx, int gridy, int gridwidth, int gridheight, double weightx, double weighty, int anchor, int fill, Insets insets, int ipadx, int ipady) {
        this.gridx=gridx;
        this.gridy=gridy;
        this.gridwidth=gridwidth;
        this.gridheight=gridheight;
        this.weightx=weightx;
        this.weighty=weighty;
        this.anchor=anchor;
        this.fill=fill;
        this.insets=insets;
        this.ipadx=ipadx;
        this.ipady=ipady;        
    }
}