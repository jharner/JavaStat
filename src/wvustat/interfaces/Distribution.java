package wvustat.interfaces;


/**
*	Distribution defines the interface for a remote probability distribution.
*
*	@author: Hengyi Xue
*	@version: 1.0, Mar. 8, 2000
*/


public interface Distribution {
	/**
	*	compute the probability density at point x
	*/
	public double pdf(double x) ;
	
	/**
	*	compute cumulative probability at point x
	*/
	public double cdf(double x) ;
	
	/**
	*	compute quantile value at point x
	*/
	public double quantile(double x) ;
}
